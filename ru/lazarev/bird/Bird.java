package ru.lazarev.bird;

/**
 * Абстрактный класс, представляющий птицу.
 * Является базовым классом для различных видов птиц.
 */
public abstract class Bird {
    protected String name;

    /**
     * Создает новый объект птицы с указанным именем.
     * @param name Имя птицы. Если имя {@code null}, будет выведено сообщение об ошибке,
     *             и имя птицы останется неинициализированным (null).
     */
    public Bird(String name) {
        if (name == null) {
            System.out.println("Ошибка: имя птицы не может быть null.");
            // this.name останется null
        } else {
            this.name = name;
        }
    }

    /**
     * Возвращает имя птицы.
     * @return Имя птицы.
     */
    public String getName() {
        return name;
    }

    /**
     * Устанавливает имя птицы.
     * @param name Новое имя птицы. Если имя {@code null}, будет выведено сообщение об ошибке,
     *             и имя птицы не будет изменено.
     */
    public void setName(String name) {
        if (name == null) {
            System.out.println("Ошибка: имя птицы не может быть null. Имя не изменено.");
        } else {
            this.name = name;
        }
    }

    /**
     * Абстрактный метод, заставляющий птицу петь.
     * Конкретная реализация пения зависит от вида птицы.
     */
    public abstract void sing();
}
